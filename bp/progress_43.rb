first_name = "Harry"
last_name = "Potter"

# Concat method overrides the variable
full_name = first_name.concat(last_name)
p first_name.concat(last_name)
puts first_name.concat(last_name)
p full_name

p first_name << last_name
puts first_name << last_name

# Prepend method overrides the variable
first_name = "Harry"
last_name = "Potter"

first_name.prepend(last_name)
p first_name.strip
